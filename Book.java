

public class Book {

    protected  String isbn;
    protected  String title;
    protected  String year;
    protected  double price;
    protected  String author;

    public Book(String isbn, String title, String year, double price, String author) {
        this.isbn = isbn;
        this.title = title;
        this.year = year;
        this.price = price;
        this.author = author;
    }
    
}

